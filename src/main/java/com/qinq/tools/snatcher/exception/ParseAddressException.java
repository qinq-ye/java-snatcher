package com.qinq.tools.snatcher.exception;

public class ParseAddressException extends SnatcherException {

	public ParseAddressException() {
		super();
	}

	public ParseAddressException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ParseAddressException(String arg0) {
		super(arg0);
	}

	public ParseAddressException(Throwable arg0) {
		super(arg0);
	}

}
